    int PassId = 0;
    private void CountPassengers()
    {
         try {
            St1 = Con.createStatement();
            Rs1 = St1.executeQuery("SELECT COUNT(*) FROM users");
            Rs1.next();
            PassId = Rs1.getInt(1) + 1;
        } catch (Exception e) {
        }
    }
    
